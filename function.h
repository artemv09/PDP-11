#ifndef FUNC
#define FUNC

typedef enum {
    // БЕЗАДРЕСНЫЕ КОМАНДЫ (полное 16-битное слово)
    OP_HALT = 0x0000,   // Останов процессора
    OP_NOP  = 0x00A0,   // Пустой такт (ничего не делать)
    OP_RESET = 0x0005,  // Сброс внешней шины
    OP_RETURN = 0x0087, // Возврат из подпрограммы (аналог RTS PC)
    OP_CCC  = 0x00AF,   // Сбросить ВСЕ флаги (C,N,V,Z = 0)
    OP_SCC  = 0x00BF,   // Установить ВСЕ флаги (C,N,V,Z = 1)
    // КОМАНДЫ РАБОТЫ С ФЛАГАМИ (Condition Code Ops)
    OP_CLC,   // Сброс флага переноса (Carry = 0)
    OP_CLN,   // Сброс флага отрицательного результата (Negative = 0)
    OP_CLV,   // Сброс флага переполнения (oVerflow = 0)
    OP_CLZ,   // Сброс флага нуля (Zero = 0)
    OP_SEC,   // Установка флага переноса (Carry = 1)
    OP_SEN,   // Установка флага отрицательного результата (Negative = 1)
    OP_SEV,   // Установка флага переполнения (oVerflow = 1)
    OP_SEZ,   // Установка флага нуля (Zero = 1)
    // КОМАНДЫ УСЛОВНОГО И БЕЗУСЛОВНОГО ПЕРЕХОДА (Branches)
    OP_BR,    // Безусловный переход (PC = PC + 2*XX)
    OP_BNE,   // Переход если НЕ равно (Z=0)
    OP_BEQ,   // Переход если равно (Z=1)
    OP_BGE,   // Переход если больше или равно (N^V=0) — для знаковых
    OP_BLT,   // Переход если меньше (N^V=1) — для знаковых
    OP_BGT,   // Переход если строго больше (Z|(N^V)=0) — для знаковых
    OP_BLE,   // Переход если меньше или равно (Z|(N^V)=1) — для знаковых
    OP_BPL,   // Переход если плюс (N=0) — число положительное
    OP_BMI,   // Переход если минус (N=1) — число отрицательное
    OP_BHI,   // Переход если выше (C|Z=0) — для беззнаковых
    OP_BLOS,  // Переход если ниже или равно (C|Z=1) — для беззнаковых
    OP_BVC,   // Переход если переполнения НЕ было (V=0)
    OP_BVS,   // Переход если переполнение БЫЛО (V=1)
    OP_BCC,   // Переход если переноса НЕ было (C=0)
    OP_BCS,   // Переход если перенос БЫЛ (C=1)
    OP_BHIS,  // Переход если выше или равно (C=0) — для беззнаковых
    OP_BLO,   // Переход если ниже (C=1) — для беззнаковых
    // СПЕЦИАЛЬНЫЕ КОМАНДЫ ПЕРЕХОДОВ И ВЫЗОВОВ
    OP_JMP,   // Безусловный прыжок по любому адресу (PC = addr)
    OP_JSR,   // Вызов подпрограммы (сохраняет PC в регистр, PC = addr)
    OP_RTS,   // Возврат из подпрограммы (PC = регистр)
    OP_SOB,   // Вычесть 1 из регистра, если не 0 — переход назад (цикл for)
    // ОДНОАДРЕСНЫЕ КОМАНДЫ (Word)
    OP_CLR,   // Обнулить приёмник (d = 0)
    OP_COM,   // Инвертировать биты (d = ~d) — побитовое НЕ
    OP_INC,   // Увеличить на 1 (d = d + 1)
    OP_DEC,   // Уменьшить на 1 (d = d - 1)
    OP_NEG,   // Изменить знак (d = -d)
    OP_ADC,   // Сложить с флагом переноса (d = d + C)
    OP_SBC,   // Вычесть флаг переноса (d = d - C)
    OP_TST,   // Проверить число (выставить флаги N, Z по d; V=0, C=0)
    OP_ROR,   // Циклический сдвиг вправо через флаг C
    OP_ROL,   // Циклический сдвиг влево через флаг C
    OP_ASR,   // Арифметический сдвиг вправо (d = d / 2) — сохраняет знак
    OP_ASL,   // Арифметический сдвиг влево (d = d * 2)
    OP_SWAB,  // Поменять местами старший и младший байты слова
    OP_SXT,   // Расширить знак (если N=1, d = -1; если N=0, d = 0)
    // ОДНОАДРЕСНЫЕ КОМАНДЫ (Byte) — то же самое, но с одним байтом
    OP_CLRB,  // Обнулить байт
    OP_COMB,  // Инвертировать байт
    OP_INCB,  // Увеличить байт на 1
    OP_DECB,  // Уменьшить байт на 1
    OP_NEGB,  // Изменить знак байта
    OP_ADCB,  // Сложить байт с флагом C
    OP_SBCB,  // Вычесть флаг C из байта
    OP_TSTB,  // Проверить байт (выставить флаги)
    OP_RORB,  // Циклический сдвиг байта вправо
    OP_ROLB,  // Циклический сдвиг байта влево
    OP_ASRB,  // Арифметический сдвиг байта вправо
    OP_ASLB,  // Арифметический сдвиг байта влево
    // ДВУХАДРЕСНЫЕ КОМАНДЫ (Word)
    OP_MOV,   // Переслать слово (d = s)
    OP_ADD,   // Сложить (d = d + s)
    OP_SUB,   // Вычесть (d = d - s)
    OP_CMP,   // Сравнить (вычислить s - d, выставить флаги, результат не сохранять)
    OP_BIT,   // Побитовое И (s & d, выставить флаги, результат не сохранять)
    OP_BIC,   // Очистить биты (d = d & ~s)
    OP_BIS,   // Установить биты (d = d | s) — побитовое ИЛИ
    // ДВУХАДРЕСНЫЕ КОМАНДЫ (Byte) — то же самое с байтами
    OP_MOVB,  // Переслать байт
    OP_CMPB,  // Сравнить байты
    OP_BITB,  // Побитовое И байтов
    OP_BICB,  // Очистить биты в байте
    OP_BISB,  // Установить биты в байте
    // РАСШИРЕННЫЕ КОМАНДЫ (EIS) — опционально
    OP_ASH,   // Арифметический сдвиг регистра (r = r * 2^s)
    OP_ASHC,  // Комбинированный арифметический сдвиг (сдвиг пары регистров)

} PDP11_Opcode;


#endif